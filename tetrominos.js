function TetrominoesRotationX(tetromino, isClockwise) {
  const rotatedTetromino = [];
  const N = tetromino.length;

  for (let i = 0; i < N; i++) {
    rotatedTetromino.push([]);
    for (let j = 0; j < N; j++) {
      rotatedTetromino[i][j] = isClockwise ? tetromino[N - j - 1][i] : tetromino[j][N - i - 1];//solo es un bucle for porque no se necesita la posicion x solo la y,z
    }
  }

  return rotatedTetromino;
}

function TetrominoesRotationY(tetromino, isClockwise) {
  const rotatedTetromino = [];
  const N = tetromino.length;

  for (let k = 0; k < N; k++) {
    rotatedTetromino.push([]);
    for (let j = 0; j < N; j++) {
      rotatedTetromino[k].push([]);
      for (let i = 0; i < N; i++) {
        rotatedTetromino[k][j][i] = isClockwise ? tetromino[ N - i - 1][j][k] : tetromino[i][j][N - k - 1];
      }
    }
  }
  return rotatedTetromino;
}



function TetrominoesRotationZ(tetromino, isClockwise) {
  const rotatedTetromino = [];
  const N = tetromino.length;


  for (let k = 0; k < N; k++) {
    rotatedTetromino.push([]);
    for (let j = 0; j < N; j++) {
      rotatedTetromino[k].push([]);
      for (let i = 0; i < N; i++) {
        rotatedTetromino[k][j][i] = isClockwise ? tetromino[k][N - i - 1][j] : tetromino[k][i][N - j - 1];
      }
    }
  }

  return rotatedTetromino;
}






